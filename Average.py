print("Problem 5: Average of two numbers program")

#ask of 2 inputs
a1 = input("Enter the 1st number: ")
a2 = input("Enter the 2nd number: ")

#Adding a group of numbers and then dividing by the count of those numbers
sum = int(a1) + int(a2)
aa = int(sum) / 2

#print the average using + to link between the words and numbers
print("The average of " + str(a1) + " and " + str(a2) + " is " + str(aa))


